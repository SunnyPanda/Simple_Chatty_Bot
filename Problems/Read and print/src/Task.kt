import java.util.*

fun main(args: Array<String>) {
    val scanner = Scanner(System.`in`)

    val firstLine = scanner.nextLine()
    val secondLine = scanner.nextLine()
    val firstNum = scanner.nextInt()
    val secondNum = scanner.nextInt()

    println(firstLine)
    println(secondLine)
    println(firstNum)
    println(secondNum)
}